      PROGRAM TRACE_NI_DRIVER
      IMPLICIT NONE
      INTEGER TRACE_LENTRIM, TRACEPATHLEN
      CHARACTER*1024 TRACEPATH
C
      TRACEPATH = ' '
      CALL GETENV('XFOIL_TRACE_DRIVER_PATH', TRACEPATH)
      TRACEPATHLEN = TRACE_LENTRIM(TRACEPATH)
      IF (TRACEPATHLEN.LE.0) STOP 'TRACE_NI_DRIVER: missing path'
C
      CALL TRACE_OPEN(TRACEPATH(1:TRACEPATHLEN))
      CALL TRACE_TEXT('driver', 'trace_ni_driver_start', ' ')
      CALL TRACE_PSWLIN_NI_TERMS('PSWLIN', 55, -1, 3, 2,
     &     1.00000000, 0.50000000,
     &     0.10000000, 0.05000000, 0.01000000, 0.02000000,
     &     0.03000000, 0.04000000, 0.06000000,
     &     0.20000000, 0.10000000, 0.07000000, 0.08000000,
     &     0.09000000, 0.15000000, 0.24000000)
      CALL TRACE_TEXT('driver', 'trace_ni_driver_end', ' ')
      CALL TRACE_CLOSE
C
      END
